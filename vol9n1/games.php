  <h2>Jason, can you comment on how your work is or is not like a game?</h2>
  <p>Many of my works are derived directly from game code. <i>This Is How You Will Die</i> uses the skeleton of an online slot machine to display its interactive narrative. <i>Ending: Chemistry,</i> a largely ignored work, never published, was created around a card game. And there are other works, like <i>The Bomar Gene,</i> that have game code within them, but where it is not a primary aspect. However the most common understanding of what a game is doesn't really apply to much digital work. Nor do I think it should.</p>
  <p>Most games have specific goals and consequences, competitions and scores. But with my work, I might have consequences in the vein of responses to user actions, but I find the notions of competition and score to be largely societal conceptions, false premises for cultural conquest.</p>
  <p>But if by game you mean play, Dear Gawd yes. I love play. If any artwork inspires play, whether on the screen or in the head's electric mass, then the creation pours awe from serving spoons, square plates, deeply filled with melted plastic and arbor day swimmers. Trees die in salt water floods, and ducks dive for the smaller, slower fish, warm feathers float longer in the cold.</p>


