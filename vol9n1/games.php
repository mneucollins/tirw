  <h2>Donna, can you comment on how your work is or is not like a game?</h2>
  <p>Two opposing characteristics are that it works on a hiding / revealing premise (a game quality), yet suggests a narrative telling by the structuring of the islands of linear animation. The project can also be seen to use repetition as a metaphor, e.g. the circular looping within the pop-in windows depicting Christian's acts of possession (metaphors tend to be used in Art projects). The two main aspects share a similar drive towards achieving a goal: an end, narratively, or a conclusion, gaming-ly. Interestingly, a discussion already exists as to whether quantifiable outcomes, achieving the goals or endings, are a prerequisite of being understood as a game.</p>
  <p>Games designer Eric Zimmerman recently stated, "A game is a system in which players engage in an artificial conflict, defined by rules, that results in a quantifiable outcome." In contrast, Greg Costikyan believes in open-ended outcomes. Sitting in the middle is Jesper Juul with his "Classic Game Model." Juul, by using a demarcated timeline, suggests that contemporary rule-based systems can be something other than the classic model where a variable and quantifiable outcome is cited. Within <i>Deviant,</i> the endpoint serves to reposition the participant back towards confusion. As seen from a gaming viewpoint, the conclusion to the project does not offer participants an understanding of their role, of what the project's ultimate goals are. It does clarify its narrative source, explaining what has been visually experienced. It does something "other" than offer a narrative or gaming ending.</p>
  <p>This newness or refusal to be categorized led  some of the expert readers to attempt a general description of the project: exploratory narrative (George Fifield), visual narrative (Jill Walker), interactive experiment in moving visual art (Mark Amerika), and interface/game/interactive environment (Cynthia Lawson).</p>
<p>Personally I find the contemporary understanding of what a (video) game is doesn't fit the behavior of my work, and the definition of what a game is and does is ready to be expanded. However I think my work "plays games" with the user in terms of its playful trixy coquettishness.</p>

