  <p>From <i>Superstitious Alliances</i></p>
  <p>Lately I've been playing with first person shooter game engines. I've been taking the open source code and fondling the folders for the images and sounds. And once I've identified the purpose of certain images, I create transparent jpegs of the same name or insert animated gifs of landscapes in the place of character movement files. Although my results so far haven't been very successful, the hope is the game will eventually look nothing like and play nothing like the original or any game.</p>
  <p>There is no way I can compete with large multi million dollar animation and coding budgets. So why not corrupt the game format entirely. To offer the hint that a user/reader/player is within a game environment, but birth their 3-dimensional experience to not resemble anything in the physical world.</p>

