  <h2>Jason, both you and Donna explore secrets in your work. Your new work <i>Hermeticon</i> aims to be an "incantation engine" with hypnotic power. Can you say more about how you made it?</h2>
  <p>The process for creating this work is a fairly good base for how I often birth these creatures. While searching for an 80's music compilation, I came across a file of over 150 commercials. Kid stuff mostly, cereal, candy, toys. I've always been fascinated by the near religious power of the hook, the catchy song line of commercials, the jingle bait. So I toyed with isolating the song hook, not only in terms of melody, but intent, the meaning of the commercial. I wanted to find the sharp edge beneath the blood worms.</p>
  <p>While testing some of my cut clips, I accidentally opened multiple files at once. The cacophony was strangely spell-like, modern incantations repeating. I've also been researching Hermetic texts by 14th-15th  century scientist/priests whose cosmology and understanding of molecular physics was alarmingly current. But often these priests would write about their ideas in chants and poetic texts. And the church regarded many as possessing evil magic, sacrilegious spells. So the two notions of hermetic texts and commercial hooks seemed perfectly disjointed and yet complementary.</p>
