  <h2>Donna, what was your process for making <i>Deviant</i>?</h2>
  <p>As <i>Deviant</i> was a key part of my PhD submission, the process was very much transparent and mapped (see Chapter 2 of my thesis <a href="http://www.6amhoover.com/viva" target="_new">http://www.6amhoover.com/viva</a>). Firstly, I came upon the narrative at a Scottish History conference where I was searching for interesting real-life sources to create a new project. The demonic possession of Christian Shaw was only mentioned in passing, but the demonizing of an 11-year-old Scottish girl, frequently painted as an evil manipulative character, was too much to pass up. I researched the differing scholarly opinions, but crucially discovered that the initial recorded written telling of the narrative was authored anonymously. These discoveries enabled me to ethically open-up, re-interpret, and re-position the various perspectives on Christian Shaw, and crucially to encourage the user to take their own stance.</p>
  <p>Once I had mapped out the skeleton of the core events, I began to embellish with additional narrative strains, many of which focused on giving Christian a presence&mdash;I hoped that this would create an emotional resonance. After the visual aesthetic was settled and the majority of the narrative constructed, I reworked the project, both whittling down the imagery and adding layers of detail. Once I had completed the whole of the narrative, I reviewed it as a user (rather than the author), seeing which tones it was "speaking with." I often find that to create vibrant interactive experiences space must be left for spontaneous or intuitive authorial creations. That done, I tested the work online and started "writing-up" what I felt were the project's interesting points, successes, and failures. Lastly, I invited a group of expert readers/users to experience the artwork. Their feedback enabled me to further frame this artwork as research.</p>
