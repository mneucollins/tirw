  <h2>Jason, what was your process for making <i>This Is How You Will Die</i>?</h2>
  <p>One: Found a slot machine fla file, the ready to go code of a game. Spent several weeks trying to redo, reconfigure, remake the code. Originally, I loaded it with sliced up scans of slides. My grandfather labeled a box of slides as "no good." After he died, while other family members barked over valuables, I slipped that box into my trunk. I am not a car, am not greased axles or roll up-roll down windows.</p>
  <p>Two: The slides, all blurred and secretly ill centered, did not fit, did not play nicely with the interface. So the file, or rather ten different files, for ten different stages of adjustment were lost amongst my cluttered folders.  </p>
  <p>Three: I cannot name files properly. Cannot keep organization, a coat pocket with receipts. Many weeks or months pass before I find this file again. An accidental mopping of the floor.</p>
  <p>Four: As a child, I did not fear death. This is not the clich&eacute; of kids feel invincible, but rather my early understanding of death as a bike spoke, if ten speeds could fly in outer space and spin out on narrow arboretum trails. Tall glass and temperature controlled. But more recently, I'm scared of dying, all middle-aged and late coming. My teeth hurt. My teeth hurt.</p>
  <p>Five: Secrets. The twin cousin, as this is not medically possible, of the secret society, the secret cure, the secret secret, the sequence of words parading for access to unmarked crates, is isolation, the alonity (alone combined with ity, and the e falls away) of long drives through fire flies. There is a story here, back rent, and a writing desk.</p>
  <p>Six: Deaths and secrets and slot machine codes.</p>
