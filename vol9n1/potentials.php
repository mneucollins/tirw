  <h2>How many are meaningful for you?  How?</h2>
  <p>I should write my own list. But then the danger of such lists is that they are contrary to the primary goal of multimodal literature. But mine would read:</p>
  <p>1  To have no specifically defined rules or goals.</p>
  <p>2  To disrupt the first goal whenever possible.</p>
  <p>That list does cover many of the benefits of our flittingly growing textual field and addresses some of the primary concerns of many artists. However, I'm hesitant to define any particular goal.</p>
  <p>Perhaps I should address these point by point:</p>
  <p>NEW TOOLS?: Tools is a terribly broad term. Which is good actually. It makes the goal much less of a goal. If by tools you mean the use of devices or hypertextual elements (like links and databases and scrollers and re-texting and others) then certainly any digital creation reinterprets those tools, uses them in new ways, and in turn creates new tools from existing tools. My car, a 1979 Toyota Corolla with a rust spot frame and a surf rack used for mattresses and thrift store finds, is now a new car with the addition of various messy, but artfully painted race symbols</p>
  <p>I certainly don't think that a work of multimodal literature needs to create new tools. In fact to some extent that makes continuing a career as a writer for this new form an impossible task. I would love to see more reuse of old tools, to more fully explore the existing tools. During my MFA studies we were forced to write sestinas and other well traveled poetic forms. We need the digital equivalent of a sestina.</p>
  <p>I'm sounding creepily critical these past few paragraphs. Hmmm...I do agree that new tools are a gorgeous and soft landing child or digital copulation. I am just hesitant to continue the New, New, New mentality of New Media.</p>
  <p>COLLABORATE?: Yes, without collaboration there would be maybe half a hundred e-lit makers in this here spinning water ball. There are not enough youngins (or oldins) out there with the complete(ish?) set of digital skills. So, if a poet wants to turn their nonlinear poem into a whirling whirligig, and unfortunately they have the electro-body field that somehow corrupts hard drives on first mouse touch, then yes they need to work with others for their vision to live.</p>
  <div><a href="images/content/nelsonpotential.jpg" target="_new"><img src="images/content/thumbs/nelsonpotential.jpg" /></a></div>
  <p>From <i>Pandemic Rooms</i></p>
  <p>There are of course innumerable levels and facets of collaboration. It could be that an artist steals code, or takes Google images, or remixes sound, basically capturing material from outside their CPU. I suppose this is collaborating. Large database works based on certain themes can be seen as collaborative, because they invite works from various artists and then either remix them or closely display them. Obviously these are only a few examples, but yes we all collaborate in some way.</p>
  <p>I of course am more than open to new input devices and have been playing recently with adapting a theremin to operate as an alternative mouse. The theremin then would encourage a broader range of motion, more closely related to dance or performance. The drawback is that the theremin is inexact, and precise mouse movement or navigation is difficult. So, in this case yes. But usually, and perhaps unfortunately, I am very unaware of my own body when I create. And don't really consider other's bodies while crafting my oddities.</p>
  <p>INTERFACE?: I love interface. This year, if I find spare hours hiding in the dark, dark morning, I will stitch a beast spun of interfaces, and only interfaces. How does one make an interface for interfaces.</p>

