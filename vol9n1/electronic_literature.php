  <h2>Jason, you've compared e-poets to wheelchair athletes:  flashy, skilled, noisy, technology-enabled, some unique talents&#8212and no crowd. Both you and Donna have developed a serious body of work over many years. Who else, in your opinion, is in this game&#8212and how do we draw in new readers and players?</h2>
  <p>Hmmm.....   </p>
  <p>Lots of possible factors here. Let's play "things that get in the way of associating oneself with e-lit."</p>
  <p>First: There is the naming difficulty, the barbarism of genre. I imagine there are many beautiful artworks, not previously associated with e-lit, but when slapped with the e-lit brand would be glorious examples. Most people know when they write poetry, or fiction (of course blurry lines and break-angst not included); however if you browse the works at Rhizome, most of what I would call e-lit there doesn't wear the name tag of e-lit.</p>
  <p>First Part Two: And within our narrow, narrow band of starry, starry e-lit stars there are debates about which work is called what. Hypermedia, hypertext, electronic lit, etc. The Poetics List recently had this discussion, which seems to listproc itself in some form every few months.</p>
  <p>Endfirst: So maybe part of the problem is a poor census, crazy rural dwellers not opening the door to strangers with pens and forms.</p>
  <p>Second: Cash, sex, fame, and free hardware.</p>
  <p>It seems in most other artforms these are real (but usually not admitted) drives. In MFA programs, some invited writers will speak of their bodily abilities or unexplainable madcap adventures. They tout their punch-punch fights with the famous poet, or the conference hallway backing of their poetic worth by those rare four-book wonders (major press only please).</p>
  <p>My point: e-lit doesn't have superstars yet. Not really. The first wave Brown kids were supposed to be ours, but not many of them are still practicing. Of course there are revered artists and writers and scholars within our wee circle. Some do cross over prettily into other fields, and we are all very famous to each other. But either we don't have the history or the gumption to build the dozens or hundreds of icons needed to inspire the youngins to play with us, to continue our good (and bad) works. To be forced to read our work and complain loudly about our lameness to chums over beers.</p>
  <p>The biggest web stars are bloggers, personalities only. So, maybe we all need to morph into opinionated blogstars. Or maybe not. Yes, not is a much improved state.</p>
  <p>Three: Where the hell do we publish our work?</p>
  <div><a href="images/content/nelsonelectronic.jpg" target="_new"><img src="images/content/thumbs/nelsonelectronic.jpg" /></a></div>
  <p>From <i>Dreamaphage</i> V.2</p>
  <p>Poetry and fiction publishers are small and poor and always struggling. But there are hundreds of them, thousands across the world.</p>
  <p>How many venues are there for e-lit? Because of this problem, I typically target net art venues or real space galleries or the like. And when you aim for gallery shows, your work begins to change; your words become circles and then faces and then dogs and horses fighting.</p>
  <p>It is good to see many universities starting to consider teaching e-lit in their courses, or supporting it with offices. Though without journals, magazines, backyard above-ground pools, we will keep sweating through the summers, wondering why the neighborhood kids don't come over for fun.</p>
  <p>Four: The circular circle.</p>
  <p>Users/Readers have to learn to read our work. But they need to read our work to learn to read our work. Universities want to teach this material, but they don't have the expertise to teach the material.</p>
  <p>Five: Will I ever stop numbering?</p>
  <p>I'll stop listing here. In summary, I am more optimistic about e-lit now than I was a year ago. My experience is that students, writers, and the broader public are growing increasingly interested in our growing area. So, if I had to offer a brief list of recommendations it would be the following:</p>
  <p>1) more venues, contests etc; 2) promotion of superstars; 3) encouraging the theorists and artists to promote their work/writings widely outside the field and academia and the art world, to promote it everywhere possible.</p>

