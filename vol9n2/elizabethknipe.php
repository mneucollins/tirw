			Elizabeth Knipe is a digital writer and experimental video artist who has a tendency to convert simple ideas into interactive electronic installations and web-based media.  Though she has a non-poetic day job, she finds that a certain poetic justice is involved.  Elizabeth has screened her videos nationwide and read at E-Poetry events in London, New York, West Virginia, and Buffalo.  She received her MFA from the Department of Media Study at the University at Buffalo.  See a selection of her work online at <a href="http://www.dreamdilation.com/" target="_blank">dreamdilation.com</a>
