			<h2> <a href="artworks/concerto/begin.html" target="_blank"> Concerto for Narrative Data </a> </h2>
			<p align="left">
						<strong>Bio</strong>
			</p>
			<p align="left">
				A magic realist whose work looks at the lives of artists, Judy Malloy works at the conjunction of poetry, hypernarrative, and information art.
			</p>
			<p align="left">
				Reviewed in <i>Postmodern Culture</i>, <i>The New York Times Book Review</i>, <i>the Washington Post</i>, <i>Modern Fiction Studies (MFS)</i>, and <i>The London Independent</i>, among many others, her work has been exhibited/published internationally including Eastgate, <i>The Iowa Review Web</i>, <i>Blue Moon Review</i>, Sao Paulo Biennial, School of the Art Institute of Chicago, Institute for Contemporary Art New Orleans, San Antonio Art Institute, P.P.O.W., The Los Angeles Institute for Contemporary Art, Heller Gallery at the University of California at Berkeley, the National Library of Madrid, The Houston Center for Photography, the Cleveland Institute of Art, Franklin Furnace, Visual Studies Workshop, San Francisco Art Institute, Tisch School of the Arts, Target Video, FLEFF Film Festival, Richmond Art Center, <i>Heresies</i>, Springer-Verlag, Tanam Press, Seal Press, E.P. Dutton, MIT Press, and the National Endowment for the Arts website.
			</p>
			<p align="left">
				A pioneer on the Internet and in electronic literature, Malloy wrote and programmed the seminal hyperfiction <i>Uncle Roger</i>. (first published on Art Com Electronic Network on the WELL in 1986) Created as an artists book and exhibited at the Richmond Art Center in 1989; published by Eastgate in 1993, Malloy's <i>its name was Penelope</i> has been called one of the classics of the "golden age" of hyperfiction by writer and critic Robert Coover.
			</p>
			<p align="left">
				As an arts journalist and arts advocate, she has worked most notably as the editor of <i>Women, Art & Technology</i>. (MIT Press, 2003) and as Editor of <i>NYFA Current</i> (formerly <i>Arts Wire Current</i>) an Internet-based National journal on social, economic, philosophical, and political issues affecting the arts and culture, sponsored by The New York Foundation for the Arts. She has also been instrumental in making the arts an integral part of the Internet. She founded the Arts Conference on the WELL, was the initial consultant for the Internet Yellow Pages, was a founding editor of the seminal <i>Leonardo Electronic News</i>, (which became <i>Leonardo Electronic Almanac</i>) introduced art students to the Internet in a series of summer workshops in the early 90's, and taught web design at the San Francisco Art Institute as a visiting faculty member. For over ten years she worked with Arts Wire and the New York Foundation for the Arts to bring artists, arts organizations, arts news, and art information onto the Internet.
			</p>
			<p align="left">
				In 1993, she was invited to Xerox PARC where she worked in CSL (Computer Science Laboratory) as the first artist in their artist-in-residence program.  From 1993-1994, she created one of the first arts websites, <i>Making Art Online</i>, currently hosted on the website of the Walker Art Center. Created on the World Wide Web in 1994, Malloy's <i>L0ve0ne</i>, one of first web-based hyperfictions, was published on Eastgate's Web Workshop in 1995. In the ensuing years, Malloy created a body of new media literature that includes <i>The Roar of Destiny Emanated from the Refrigerator</i>, (1995-1999) <i>Revelations of Secret Surveillance </i>(2004-2006) and the hyperlibretto, <i>The Wedding Celebration of Gunter and Gwen</i>. (2007)
			</p>
			<p align="left">
				She believes that ideally, print literature and e-literature; sequential literature and hyperfiction; painting and new media; are parallel art forms where writers in each medium understand each other's vision and, as between poetry and fiction, sometimes move with ease between the mediums.
			</p>
			<p align="left">
				"My work looks at society in multiple ways -- ranging from the bawdy feminist take on office politics in 500 3X5 Cards and Other Stories to the poetic sorrow expressed in Ask for Sanctuary," she states. "For all societies -- from ancient Greek to contemporary times -- the freedom to make art in such diverse ways is of primary importance in the creation of vibrant and lasting cultures."
			</p>

