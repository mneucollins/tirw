			<h2><br />Introduction</h2>

			<h3>
				Some Joyces, Not an Eco
				
			</h3>
			<h2>
				Stuart Moulthrop
			</h2>
			<p align="left">
				The genealogy of <i>instruments and playable texts</i>, considered as a literary (or essential fictional) micro-movement, is best understood in terms of transition, or a series of transits.
			</p>
			<p align="left">
				<strong>Transit 1: De Paris &agrave; Liberty City en quatre.</strong>
			</p>
			<p align="left">
				First of course came <i>litt&eacute;rature potentielle</i>, that great investigation of possibility not just in writing, but in the act of writing: and with it, the notion that literary texts might be held open not just in theory but in transmission, reception, and most novel of all, <i>operation</i>.  It needs then just a quick hop over channel or ocean to ferry us from Georges Perec or Harry Mathews to John Cayley and Michael Joyce, from new structures for literature to various kinds of writing-forward: say "programmatology," or "a structure for what does not yet exist."
			</p>
			<p align="left">
				So, having anagrammatized LI-PO into its germinal <i>mode d'emPLOI</i>, we translate ourselves into the Anglo-Russian idiom, and its great pastime, word golf.  What is this fascination with flickering, digital signifiers, but a PLOY on the part of certain literary vagabonds, the likes of Judy Malloy, Deena Larsen, Shelley Jackson, or Jane Douglas, to mix it up across the Two Cultures, con-fusing the domains of computation and writing?  And what would be the aim of all this tricksterei, clearly on evidence at sites like <i>Grand Text Auto</i>, but to open the doors of the library and make it once again a place of PLAY?
			</p>
			<p align="left">
				<strong>Transit 2: From Diotallevi to Wikipedia</strong>
			</p>
			<p align="left">
				Surprisingly, just as there may actually be one American not deeply fanatical about baseball (that would be me), there are also writers, even great ones, who seem to dislike word golf.  That would be the Eco of <i>Foucault's Pendulum</i>, or the particular projection or Ecologue who makes his character Diotallevi say this:
			</p>
			<p align="left" class="in1">
				My cells have learned that you can blaspheme by anagrammatizing the Book, and all the books of the world.  And they have learned to do this now with my body.  They invert, transpose, alternate, transform themselves into cells unheard of, new cells without meaning, or with meaning contrary to the right meaning.  There must be a right meaning and a wrong meaning: otherwise you die.  My cells joke, without faith, blindly. (566)</p>
			<p align="left">
				I recall hearing these words, taken rather loosely from their complex, fictive context, quoted at a meeting of the <i>soi-disant</i> Modern Language Association, in a passionate complaint against electronic writing.  This was long ago, in another century.  <i>Modo</i> is of course an inherently moving target, and what was <i>&agrave; la mode</i> before the Millennium may now appear ponderously (if not pedantically) antique.  Still, it is worth remembering that some people once keenly feared what might happen if, as I like to say, the word will not be still.  Dire things would follow, we were told.  The end of learning, the decline of the west, foreclosure of the American mind.  Pain.  Death.  Cancer.
			</p>
			<p align="left">
				These last three are indeed not joking matters, as at least one of them must come to you and me.  You do not have to share my family history of acute Puritanism to feel exposed to the brimstone preacher's agenbite: <i>hey you with the goofy grin, leave off  that sinful foolery -- don't you know you're going to die?</i>

			</p>
			<p align="left">
				As an opening to moral instruction, this trope is about as effective as its secular equivalent, the commercial message.  Hey you with the goofy grin, couldn't those teeth be brighter?  Remember to pick up whitening strips on way home from church.  As literary criticism or polemic, however, Diotallevi's deathbed rant carries considerably less impact, for its dire vision presumes a belief in the fragility of alphabets (textual and genetic) which has become increasingly hard to sustain.  On the literal side of Eco's metaphor, we now learn that the so-called decoding of the human genome only serves as prelude to still harder subjects like proteomics and morphogenetics.  If the body is in any sense alphabetic, the language implied by that alphabet still eludes human comprehension.  Meanwhile, on the figurative (or literary) side of the street, we read this:
			</p>
			<p align="left" class = "in1">
				<i>Foucault's Pendulum</i> (original title: <i>Il pendolo di Foucault</i>) is a <a href="http://en.wikipedia.org/wiki/Novel" target="_blank">novel</a> by <a href="http://en.wikipedia.org/wiki/Italy" target="_blank">Italian</a> <a href="http://en.wikipedia.org/wiki/Novelist" target="_blank">novelist</a> and <a href="http://en.wikipedia.org/wiki/Philosopher" target="_blank">philosopher</a> <a href="http://en.wikipedia.org/wiki/Umberto_Eco" target="_blank">Umberto Eco</a>. It was first published in <a href="http://en.wikipedia.org/wiki/1988" target="_blank">1988</a>; the translation into English by <a href="http://en.wikipedia.org/wiki/William_Weaver" target="_blank">William Weaver</a> appeared a year later.
			</p>
			<p align="left" class = "in1">
				<i>Foucault's Pendulum</i> is divided into ten segments represented by the ten <a href="http://en.wikipedia.org/wiki/Sephirot_(Kabbalah)" target="_blank">Sefiroth</a>. The novel is full of <a href="http://en.wikipedia.org/wiki/Esoteric" target="_blank">esoteric</a> references to the <a href="http://en.wikipedia.org/wiki/Kabbalah" target="_blank">Kabbalah</a>, <a href="http://en.wikipedia.org/wiki/Alchemy" target="_blank">alchemy</a> and <a href="http://en.wikipedia.org/wiki/Conspiracy_theory" target="_blank">conspiracy theory</a>, so many that critic and novelist <a href="http://en.wikipedia.org/wiki/Anthony_Burgess" target="_blank">Anthony Burgess</a> has suggested that it needed an index. The title of the book derives from an <a href="http://en.wikipedia.org/wiki/Foucault_pendulum" target="_blank">actual pendulum</a> designed by the French physicist <a href="http://en.wikipedia.org/wiki/Le%CC%81on_Foucault" target="_blank">L&eacute;on Foucault</a> to demonstrate the rotation of the earth.
				(Wikipedia)
			</p>
			<p align="left">
				This text is also no joke, or anything especially playful, in the sense of exuberance or elegance, but it does engage one sense of PLAY that Eco's doomed cabalist, and his admirers in the MLA, jointly disclaim.  It was produced not by a single, authorial agency with some royal or corporate patent of "truth," but rather through a scheme of voluntary production, editing, and review.  It is wiki-work, the dynamic, communal, and no doubt imperfect convergence of free-flickering signifiers unto a generally accepted signified.
			</p>
			<p align="left">
				Things are different in the young millennium, where our <i>wiki-wiki</i> world changes fear to hope.  Behold, a few thousand code monkeys banging away at their keyboards produce a passable facsimile of <i>World Book</i>, if not <i>Britannica</i>.  Open-source programming offers up legitimate, robust alternatives to corporate bloatware.  Massively multiplayer online games and social networking sites create new channels for contact and community.  No doubt these are all brave new worlds -- as Prospero snaps, <i>brave to thee</i> -- and thus about as perfect as no utopia can ever be.  Yet for old men like me, who stand skeptically on the margins, secretly mourning our drown&eacute;d book, it is impossible to deny, if we owe anything to truth, that these unlikely systems work far better than we guessed
			</p>
			<p align="left">
				So the times change, and text becomes playable, or is placed in play, just as our Oulipist masters intended; or even as not.  Sometimes, perhaps, play evolves in directions we do not anticipate.  Foreknowledge is necessarily imperfect: if we could predict the future, who would ever go there? 
			</p>
			<p align="left">
				<strong>Transit 3: Playlist</strong>
			</p>
			<p align="left">
				To perform this final transit, from then to now to hereafter, we must comb the unfamiliar, bringing out demonstrations and inventions, high gimmickry and thought-experiments, unspeakable practices and arcane instruments.  Sometimes we understand these new organs in the musical or aesthetic sense, as the apparatus for melody, harmony, and joyful noise.  Then again, we may see them in a more scientific vein, as instruments of discovery -- in this sense indeed like the pendulum of Eco and Foucault, though less beholden, perhaps, to any sort of Gravity.  In the end, our play upon these instruments, in the echo chamber of reverberating culture, may incline more to one Foucault than the other, though it must always include multiple Joyces.  We want as much genius as we can find, so let us invite one more of that sort from the wings, along with his balladeer.  Consider Jonathan Coulton on the great geometer, Benoit Mandelbrot:
			</p>
			<p align="left" class = "in1">
				He saw that infinite complexity could be described by simple rules <br /> 
				He used his giant brain to turn the game around
			</p>
			<p align="left">
				Though some of us (having got the memo) might wish for nicer brains (I would settle for Coulton's), the ludic scientists whose work you will find here all play some version of Mandelbrot's revolutionized game.  Our work is animated by the desire to evoke from simple rules a plausibly infinite set of expressions.  We come at this problem from various perspectives, techniques, and points of the aesthetic compass, and we arrive at happily different results, but a certain resemblance remains.
			</p>
			<p align="left">
				For Judy Malloy, who was a master composer when I was still learning canon and fugue, the key to invention lies in the artful crossing of pattern and chance, of musical and cybernetic form, in her "Concerto for Narrative Data."
			</p>
			<p align="left">
				John Cayley, who would be our Che or Tristan Tzara if this were an actual movement, gives us a newly re-engineered version of "riverIsland," an exploration of poetry-as-simulation that continues to define the possibilities of its form.
			</p>
			<p align="left">
				Next come some younger though no less accomplished talents, beginning with Shawn Rider, a writer, digital designer, and meta-gamer who is represented here with two pieces, "PiTp," a work laid open deliberately to digital intervention, and "So Random," a story that tells itself each time, specially, just for you.
			</p>
			<p align="left">
				Elizabeth Knipe, another relatively new player, offers "activeReader," an interactive media piece that brings its own interpretation of reader engagement and emergent, open form.
			</p>
			<p align="left">
				Nick Montfort, equally at ease with aesthetic programming and the long-form palindrome, offers what we might call a minimum instrument, "The Purpling," a maze of recirculating expression built from humble Web pages.
			</p>
			<p align="left">
				Last in train is my own "Under Language," a sort of talkative poem with consequences, far less credible in its claim to infinity than most of its companions, but still a kind of game, for those who will play.

			</p>
