			<strong>A note from the managing editor</strong>
			</p>
			<p>
				The boundaries that once distinguished various media-centricities have long since disappeared into a melting pot of media-flux. Tangible ideas formed of stone have given way to abstracted binary representations, and are now reconstituted and given meaning by the reader, whose agency creates their form. Within this paradigm shift, the author/artist is dead, but the instrument-maker lives on. They are the ones who now shape our encounter with ideas and open up channels of deep understanding. All we need do is pick up the instrument and play.
			</p>
			<p>
				As an artist with a keen interest in art history and new media, I am pleased to have had a front-row seat for this ongoing revolution. It is a fascinating time for participatory observation, and for much of this time, <i>The Iowa Review Web</i> has been there with me. As you might guess, I was delighted to accept the invitation to serve as managing editor of TIRW-vol9n2. It has been a great pleasure working with Stuart and the participating authors to bring this edition to you. Having become intimately familiar with each of the pieces presented here, I can assure you...you are going to enjoy this.
